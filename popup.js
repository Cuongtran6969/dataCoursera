const autoWorkCoursera = (data) => {
    let currentWeb = window.location.href
    let listQuestion
    data.map((item) => {
        if (item.link.includes(currentWeb)) {
            listQuestion = item.questionList
        }
    })
    let listGroupQuestion = document.querySelectorAll('.rc-FormPartsQuestion')
    listGroupQuestion.forEach((groupItem) => {
        let groupContentCell = groupItem.querySelectorAll('.rc-FormPartsQuestion__contentCell')
        //question part
        //list have two p tag
        let questionItems = groupContentCell[0].getElementsByTagName('p')
        let firstItem = groupContentCell[0].getElementsByTagName('p')[0].textContent
        let questionItem = firstItem != "" ? firstItem : questionItems[questionItems.length - 1].textContent
        //answer part
        let groupContent = groupContentCell[1]
        let typeQuestion = groupContent.querySelectorAll('div')[0];
        console.log(questionItem);
        //group answer
        let idFind;

        for (let i = 0; i < listQuestion.length; i++) {
            if (questionItem.includes(listQuestion[i].question)) {
                idFind = listQuestion[i].id;
                break;
            }
        }

        if (idFind != null) {
            let listChooseAnswer = typeQuestion.querySelectorAll('span._bc4egv.p-x-1s.font-weight-normal')
            //list choose
            let havefound = false
            for (let m = 0; m < listChooseAnswer.length; m++) {
                //list answer
                for (let n = 0; n < listQuestion[idFind].answer.length; n++) {
                    if ((listChooseAnswer[m].innerText == listQuestion[idFind].answer[n]) || listChooseAnswer[m].innerText.includes(listQuestion[idFind].answer[n])) {
                        listChooseAnswer[m].click()
                        havefound = true;
                    }
                }
            }
            if (!havefound) {
                appendAnswer(listQuestion, idFind)
                // console.log('here');
            }
        }

        function appendAnswer(listQuestion, idFind) {
            if (groupContent.querySelector('.answer_input') == null) {
                let answerInput = document.createElement('p');
                answerInput.className = 'answer_input'
                answerInput.textContent = `${listQuestion[idFind].answer}`
                answerInput.style.color = 'white'; // Màu sắc chữ
                answerInput.style.padding = '5px'
                answerInput.style.backgroundColor = '#2a73cc'; // Màu nền
                groupContent.appendChild(answerInput);
            }
        }
    })
    document.querySelectorAll('.cds-checkboxAndRadio-label')[0].click()
}

